package Teste;

public class PassarId {

    private int id;
    private String nome;
    private String username;
    private String senha;
    private int idade;
    
    public PassarId() {
        this.id = 1;
        this.id = id;
        this.idade = idade;
        this.nome = nome;
        this.senha = senha;
        this.username = username;
    }

    public int getId() {
        this.id = 2 + id;
        return id;
    }

    
    public void setId(int id) {
        this.id = id;
    }


    public String getNome() {
        return nome;
    }

    
    public void setNome(String nome) {
        this.nome = nome;
    }

   
    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getSenha() {
        return senha;
    }
    
    public void setSenha(String senha) {
        this.senha = senha;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }
    
}
